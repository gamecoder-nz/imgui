    inline ImTextureID toId(bgfx::TextureHandle _handle, uint8_t _flags, uint8_t _mip)
    {
        union { struct { bgfx::TextureHandle handle; uint8_t flags; uint8_t mip; } s; ImTextureID id; } tex;
        tex.s.handle = _handle;
        tex.s.flags = _flags;
        tex.s.mip = _mip;
        return tex.id;
    }

